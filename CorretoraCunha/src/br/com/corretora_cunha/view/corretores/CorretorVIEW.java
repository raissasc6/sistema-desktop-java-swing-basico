    private void preencheTabela() {
        try {
            //Limpa todas as linhas
            modelo_jtl_consultar_cor.setNumRows(0);

            rs = corretorCTR.consultarCorretor(corretorDTO, 5);
            //Enquanto tiver linhas - fa√ßa
            while (rs.next()) {
                modelo_jtl_consultar_cor.addRow(new Object[]{
                    rs.getString("id_cor"),
                    rs.getString("nome_cor"),
                    rs.getString("cidade_cor"),
                    rs.getString("tel_cor"),
                    rs.getString("email_cor"),});
            }
        } catch (Exception erTab) {
            System.out.println("Erro SQL: " + erTab);
        }
    }//Fecha m√©todo preencheTabela(String nom_pes)
     
    private void pesquisaTabela(String cidade_cor) {
        try {
            //Limpa todas as linhas
            modelo_jtl_consultar_cor.setNumRows(0);

            corretorDTO.setCidade_cor(cidade_cor);
            rs = corretorCTR.consultarCorretor(corretorDTO, 3);
            //Enquanto tiver linhas - fa√ßa
            while (rs.next()) {
                modelo_jtl_consultar_cor.addRow(new Object[]{
                    rs.getString("id_cor"),
                    rs.getString("nome_cor"),
                    rs.getString("cidade_cor"),
                    rs.getString("tel_cor"),
                    rs.getString("email_cor"),});
            }
        } catch (Exception erTab) {
            System.out.println("Erro SQL: " + erTab);
        }
    }//Fecha m√©todo preencheTabela(String nom_pes)
    
    private void gravar_corretor() {
        negocioCorDTO.setId_neg(this.id_neg);
        negocioCorDTO.setId_cor(corretorDTO.getId_cor());
        JOptionPane.showMessageDialog(null,
                negocioCorCTR.inserirNegocioCor(negocioCorDTO)
        );
    }//Fecha m√©todo gravar()
    
    
    
     
